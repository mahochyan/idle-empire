// 字符网格 → 矩形合并
function gridToRects(grid,palette){
  const H=grid.length,W=grid[0].length;
  const vis=Array.from({length:H},()=>new Array(W).fill(false));
  const rects=[];
  for(let y=0;y<H;y++){
    for(let x=0;x<W;x++){
      if(vis[y][x]||grid[y][x]==='.')continue;
      const ch=grid[y][x];
      const c=palette[ch];
      if(!c)continue;
      let rw=1;
      while(x+rw<W&&grid[y][x+rw]===ch&&!vis[y][x+rw])rw++;
      let rh=1;
      while(y+rh<H){
        let ok=true;
        for(let dx=0;dx<rw;dx++){if(grid[y+rh][x+dx]!==ch||vis[y+rh][x+dx]){ok=false;break}}
        if(!ok)break;
        rh++;
      }
      for(let dy=0;dy<rh;dy++)for(let dx=0;dx<rw;dx++)vis[y+dy][x+dx]=true;
      rects.push([x,y,rw,rh,c]);
    }
  }
  return rects;
}
